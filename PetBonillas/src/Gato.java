public class Gato {
    public String nome;
    public int id;
    public String raca ;
    public boolean pelos;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getRaca() {
        return raca;
    }

    public void setRaca(String raca) {
        this.raca = raca;
    }

    public boolean isPelos() {
        return pelos;
    }

    public void setPelos(boolean pelos) {
        this.pelos = pelos;
    }
}
